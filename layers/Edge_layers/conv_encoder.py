import torch
from torch import nn
from timm.models.layers import DropPath
from layers.Edge_layers.custom_layers import LayerNorm
from layers.Edge_layers.LoRaLin import LoRaLin_KAN, LoRaLin
from layers.KAN_layers.KANConv import KAN_Convolutional_Layer

class ConvEncoder_KANConv(nn.Module):
    def __init__(self, dim, drop_path=0., layer_scale_init_value=1e-6, expan_ratio=4, kernel_size=7):
        super().__init__()
        self.dwconv = KAN_Convolutional_Layer(dim, dim, kernel_size=kernel_size, padding=(kernel_size[0] // 2, kernel_size[1] // 2))
        self.norm = LayerNorm(dim, eps=1e-6)
        self.pwconv1 = LoRaLin(dim, expan_ratio * dim)
        self.act = nn.GELU()
        self.pwconv2 = LoRaLin(expan_ratio * dim, dim)
        self.gamma = nn.Parameter(layer_scale_init_value * torch.ones(dim),
                                  requires_grad=True) if layer_scale_init_value > 0 else None
        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()

    def forward(self, x):
        input = x
        x = self.dwconv(x)
        x = x.permute(0, 2, 3, 1)  # (N, C, H, W) -> (N, H, W, C)
        x = self.norm(x)
        x = self.pwconv1(x)
        x = self.act(x)
        x = self.pwconv2(x)
        if self.gamma is not None:
            x = self.gamma * x
        x = x.permute(0, 3, 1, 2)  # (N, H, W, C) -> (N, C, H, W)

        x = input + self.drop_path(x)
        return x

